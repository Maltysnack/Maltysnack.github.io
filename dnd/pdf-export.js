const ATTACK_SLOTS = [
  { name: 'Wpn Name',   atk: 'Wpn1 AtkBonus',   dmg: 'Wpn1 Damage' },
  { name: 'Wpn Name 2', atk: 'Wpn2 AtkBonus ',  dmg: 'Wpn2 Damage ' },
  { name: 'Wpn Name 3', atk: 'Wpn3 AtkBonus  ', dmg: 'Wpn3 Damage ' },
];

function fmtMod(n) { return (n >= 0 ? '+' : '') + n; }
function abilityMod(score) { return Math.floor((score - 10) / 2); }

function safeSet(form, name, value) {
  try {
    const f = form.getTextField(name);
    f.setText(String(value ?? ''));
  } catch (e) {
    // field doesn't exist or wrong type, skip silently
  }
}

function safeCheck(form, name, on) {
  try {
    const cb = form.getCheckBox(name);
    if (on) cb.check(); else cb.uncheck();
  } catch (e) {}
}

export async function fillCharacterPdf(character, state, templateBuffer, PDFDocument) {
  const pdfDoc = await PDFDocument.load(templateBuffer);
  const form = pdfDoc.getForm();

  const c = character;
  const s = state;

  const profBonus = c.profBonus;
  const abilities = Object.fromEntries(c.abilities.map(a => [a.key, a.score]));
  const mods = Object.fromEntries(c.abilities.map(a => [a.key, abilityMod(a.score)]));
  const spellAbility = c.spellcastingAbility;

  // Header / identity
  safeSet(form, 'CharacterName', c.identity.name);
  safeSet(form, 'ClassLevel',    c.identity.classLine);
  safeSet(form, 'Background',    c.identity.background);
  safeSet(form, 'Race ',         c.identity.classLine?.split(/\s+/)[0] || '');
  safeSet(form, 'Alignment',     s.alignment || '');
  safeSet(form, 'XP',            s.xp || '');
  safeSet(form, 'Speed',         (c.identity.speedLine || '').replace(/^Speed\s*/i, ''));

  // Ability scores + mods
  for (const [key, fields] of Object.entries(ABILITY_FIELDS)) {
    safeSet(form, fields.score, abilities[key]);
    safeSet(form, fields.mod, fmtMod(mods[key]));
  }

  // Saves: bonus text + proficient checkbox
  for (const save of (c.saves || [])) {
    const bonus = mods[save.ability] + (save.proficient ? profBonus : 0);
    safeSet(form, SAVE_TEXT[save.ability], fmtMod(bonus));
    safeCheck(form, ABILITY_FIELDS[save.ability].saveProf, save.proficient);
  }

  // Skills: bonus text + proficient checkbox
  for (const sk of (c.skills || [])) {
    const fields = SKILL_FIELDS[sk.name];
    if (!fields) continue;
    const bonus = mods[sk.ability] + (sk.proficient ? profBonus : 0);
    safeSet(form, fields.text, fmtMod(bonus));
    safeCheck(form, fields.prof, sk.proficient);
  }

  // Combat
  safeSet(form, 'AC',         c.combat.ac);
  safeSet(form, 'Initiative', fmtMod(mods.dex));
  safeSet(form, 'ProfBonus',  fmtMod(profBonus));
  safeSet(form, 'Inspiration', s.inspiration ? '1' : '');
  safeSet(form, 'Passive',    10 + mods.wis + (c.skills?.find(x => x.name === 'Perception')?.proficient ? profBonus : 0));
  safeSet(form, 'HPMax',      c.combat.hpMax);
  safeSet(form, 'HPCurrent',  s.hp.current);
  safeSet(form, 'HPTemp',     s.hp.temp || '');
  safeSet(form, 'HDTotal',    `${c.combat.hitDiceMax}${c.combat.hitDiceType}`);
  safeSet(form, 'HD',         s.hitDice);

  // Death saves
  DEATH_SUCCESS.forEach((cb, i) => safeCheck(form, cb, !!s.deathSaves?.successes?.[i]));
  DEATH_FAILURE.forEach((cb, i) => safeCheck(form, cb, !!s.deathSaves?.failures?.[i]));

  // Spell DC + atk
  if (spellAbility && mods[spellAbility] !== undefined) {
    safeSet(form, 'SpellSaveDC  2',   8 + profBonus + mods[spellAbility]);
    safeSet(form, 'SpellAtkBonus 2',  fmtMod(profBonus + mods[spellAbility]));
    safeSet(form, 'Spellcasting Class 2', c.identity.classLine);
    safeSet(form, 'SpellcastingAbility 2', spellAbility.toUpperCase());
  }

  // Currencies (all into GP for now since the engine only tracks gold)
  safeSet(form, 'GP', s.gold ?? 0);

  // Languages + Proficiencies (combined, the PDF has one field)
  const profs = [c.identity.languages, c.identity.otherProficiencies].filter(Boolean).join('\n');
  safeSet(form, 'ProficienciesLang', profs);

  // Attacks: first 3 weapons go to the table rows, rest goes to the
  // AttacksSpellcasting overflow text field.
  const attacks = c.attacks || [];
  attacks.slice(0, 3).forEach((a, i) => {
    safeSet(form, ATTACK_SLOTS[i].name, a.name);
    safeSet(form, ATTACK_SLOTS[i].atk,  a.atk);
    safeSet(form, ATTACK_SLOTS[i].dmg,  a.notes);
  });
  const overflowAttacks = attacks.slice(3).map(a => `${a.name} ${a.atk} - ${a.notes}${a.extra ? ' (' + a.extra + ')' : ''}`);
  // Plus reaction
  const reactionLine = c.reaction ? [`Reaction: ${c.reaction.title} - ${c.reaction.desc}`] : [];

  // Spells: place each known spell in the right level row by order.
  // Track overflow per level.
  const overflow = [];
  if (Array.isArray(c.spells)) {
    const used = {};
    for (const sp of c.spells) {
      const lvl = sp.lvl ?? 0;
      const rows = SPELL_ROWS[lvl];
      if (!rows) continue;
      const idx = used[lvl] = (used[lvl] || 0);
      if (idx < rows.length) {
        safeSet(form, rows[idx], sp.name);
        used[lvl] = idx + 1;
      } else {
        overflow.push(`${sp.lvlLabel || 'lvl ' + lvl}: ${sp.name}`);
      }
    }
  }
  // Limited spells (racials, scrolls) get appended to overflow with a tag
  if (Array.isArray(c.limitedSpells)) {
    for (const ls of c.limitedSpells) {
      overflow.push(`${ls.group}: ${ls.name}${ls.meta ? ' (' + ls.meta + ')' : ''}`);
    }
  }

  // AttacksSpellcasting: reaction + attack overflow + spell overflow + spellSource note
  const atkSpellLines = [
    ...reactionLine,
    ...(overflowAttacks.length ? ['Extra attacks:', ...overflowAttacks.map(l => '  ' + l)] : []),
    ...(overflow.length ? ['Overflow spells:', ...overflow.map(l => '  ' + l)] : []),
    ...(c.spellSource ? [c.spellSource] : []),
  ];
  if (atkSpellLines.length) safeSet(form, 'AttacksSpellcasting', atkSpellLines.join('\n'));

  // Equipment: dump inventory list (state.inventory is the runtime version
  // including session adds; falls back to character.defaultInventory)
  const inv = (s.inventory && s.inventory.length) ? s.inventory : (c.defaultInventory || []);
  const eqLines = inv.map(i => i.notes ? `${i.name} - ${i.notes}` : i.name);
  safeSet(form, 'Equipment', eqLines.join('\n'));

  // Features and Traits: features list + racial traits + class resources
  // (which the PDF has no slots for, so they go here as text)
  const featLines = [];
  if (c.racialTraits?.length) {
    featLines.push('Racial traits:');
    c.racialTraits.forEach(t => featLines.push(`  ${t.name}${t.desc ? ' - ' + t.desc : ''}`));
    featLines.push('');
  }
  if (c.resources?.length) {
    featLines.push('Class resources:');
    c.resources.forEach(r => {
      const stateVal = s.resources?.[r.key];
      let cur;
      if (r.type === 'pip' && Array.isArray(stateVal)) cur = stateVal.filter(v => !v).length;
      else if (r.type === 'counter') cur = stateVal;
      else if (r.type === 'toggle') cur = stateVal ? 'used' : 'available';
      else cur = '?';
      featLines.push(`  ${r.name}: ${cur}/${r.max ?? '-'} (${r.restoresOn} rest)`);
    });
    featLines.push('');
  }
  if (c.features?.length) {
    let lastGroup = null;
    c.features.forEach(f => {
      if (f.group && f.group !== lastGroup) {
        featLines.push(`${f.group}:`);
        lastGroup = f.group;
      }
      const indent = f.group ? '  ' : '';
      const tag = f.tag ? ` (${f.tag})` : '';
      const upcoming = f.upcoming ? ' [upcoming]' : '';
      featLines.push(`${indent}${f.name}${tag}${upcoming}: ${f.desc}`);
    });
  }
  safeSet(form, 'Features and Traits', featLines.join('\n'));

  // Treasure: any homebrew rules text
  if (c.homebrew && c.homebrew.trim()) {
    safeSet(form, 'Treasure', c.homebrew);
  }

  // Flatten? Keep editable so the player can hand-edit afterwards.
  // form.flatten();

  return await pdfDoc.save();
}
