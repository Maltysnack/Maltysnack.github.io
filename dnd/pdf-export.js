/* =====================================================================
   pdf-export.js

   Fills the WotC fillable PDF (assets/5E_CharacterSheet_Fillable.pdf) with
   a character's data + current session state, returns the bytes for
   download. Lazy-loaded by sheet.js on Download PDF button click.

   Design notes:
   * Field names taken directly from the PDF AcroForm (some have trailing
     spaces, e.g. "Race ", "DEXmod ", "Deception "). Match exactly.
   * Spell row -> level mapping is derived from on-page geometry, not
     numeric order. The mapping below was generated by inspecting each
     "Spells <id>" field's y position on page 2.
   * Things the PDF can't represent (resource pips, racial trait list,
     subclass spell-list note) get dumped into the Features and Traits
     and Equipment text fields with structured prefixes.
   ===================================================================== */

